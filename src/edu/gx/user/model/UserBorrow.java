package edu.gx.user.model;
import java.sql.Timestamp;
public class UserBorrow {
         private Integer userID;
         private int roleID;
         private  int borrowBooksLength;
         private  Timestamp borrowDate;
         private  int borrowID;
         private String mail;
         private String userName;
         private String bookName;
		public Integer getUserID() {
			return userID;
		}
		public void setUserID(Integer userID) {
			this.userID = userID;
		}
		public int getRoleID() {
			return roleID;
		}
		public void setRoleID(int roleID) {
			this.roleID = roleID;
		}
		public int getBorrowBooksLength() {
			return borrowBooksLength;
		}
		public void setBorrowBooksLength(int borrowBooksLength) {
			this.borrowBooksLength = borrowBooksLength;
		}
		public Timestamp getBorrowDate() {
			return borrowDate;
		}
		public void setBorrowDate(Timestamp borrowDate) {
			this.borrowDate = borrowDate;
		}
		
		public int getBorrowID() {
			return borrowID;
		}
		public void setBorrowID(int borrowID) {
			this.borrowID = borrowID;
		}
		public String getMail() {
			return mail;
		}
		public void setMail(String mail) {
			this.mail = mail;
		}
		public String getUserName() {
			return userName;
		}
		public void setUserName(String userName) {
			this.userName = userName;
		}
		
		public String getBookName() {
			return bookName;
		}
		public void setBookName(String bookName) {
			this.bookName = bookName;
		}
		@Override
		public String toString() {
			return "UserBorrow [userID=" + userID + ", roleID=" + roleID
					+ ", borrowBooksLength=" + borrowBooksLength
					+ ", borrowDate=" + borrowDate + ", borrowID=" + borrowID
					+ ", mail=" + mail + ", userName=" + userName
					+ ", bookName=" + bookName + "]";
		}
		
		 
}
