package com.bs.base.test;

import java.security.GeneralSecurityException;
import java.util.Properties;

import javax.mail.Message;
import javax.mail.MessagingException;
import javax.mail.NoSuchProviderException;
import javax.mail.Session;
import javax.mail.Transport;
import javax.mail.internet.InternetAddress;
import javax.mail.internet.MimeMessage;

import com.sun.mail.util.MailSSLSocketFactory;

public class Test {
      public static void main(String[] args) {
    		mail("1069871455@qq.com","mybatis plus");//发送邮件
	}
      public static void mail(String mail,String title){
 		 Properties prop = new Properties();
 	        // 开启debug调试，以便在控制台查看
 	        prop.setProperty("mail.debug", "true");
 	        // 设置邮件服务器主机名
 	        prop.setProperty("mail.host", "smtp.qq.com");
 	        // 发送服务器需要身份验证
 	        prop.setProperty("mail.smtp.auth", "true");
 	        // 发送邮件协议名称
 	        prop.setProperty("mail.transport.protocol", "smtp");

 	        // 开启SSL加密，否则会失败
 	        MailSSLSocketFactory sf = null;
 			try {
 				sf = new MailSSLSocketFactory();
 			} catch (GeneralSecurityException e) {
 				// TODO Auto-generated catch block
 				e.printStackTrace();
 			}
 	        sf.setTrustAllHosts(true);
 	        prop.put("mail.smtp.ssl.enable", "true");
 	        prop.put("mail.smtp.ssl.socketFactory", sf);

 	        // 创建session
 	        Session session = Session.getInstance(prop);

 	        // 通过session得到transport对象
 	        Transport ts = null;
 			try {
 				ts = session.getTransport();
 			} catch (NoSuchProviderException e1) {
 				// TODO Auto-generated catch block
 				e1.printStackTrace();
 			}
 	        // 连接邮件服务器：邮箱类型，帐号，授权码代替密码（更安全）
 	        try {
 				ts.connect("smtp.qq.com", "1010789554", "ukgyonykdfjpbfib");
 			} catch (MessagingException e1) {
 				// TODO Auto-generated catch block
 				e1.printStackTrace();
 			}
 	        // 后面的字符是授权码，用qq密码失败了

 	        // 创建邮件
 	        Message message = null;
 			try {
 				message = createSimpleMail(session,mail,title);
 			} catch (Exception e) {
 				// TODO Auto-generated catch block
 				e.printStackTrace();
 			}
 	        // 发送邮件
 	        try {
 				ts.sendMessage(message, message.getAllRecipients());
 			} catch (MessagingException e) {
 				// TODO Auto-generated catch block
 				e.printStackTrace();
 			}
 	        try {
 				ts.close();
 			} catch (MessagingException e) {
 				// TODO Auto-generated catch block
 				e.printStackTrace();
 			}
 	}
      /**
       * @Method: createSimpleMail
       * @Description: 创建一封只包含文本的邮件
       */
      public static MimeMessage createSimpleMail(Session session,String mail,String title)
              throws Exception {
          // 创建邮件对象
          MimeMessage message = new MimeMessage(session);
          // 指明邮件的发件人
          message.setFrom(new InternetAddress("1010789554@qq.com"));
          // 指明邮件的收件人
          message.setRecipient(Message.RecipientType.TO, new InternetAddress(mail.toString().trim()));
          // 邮件的标题
          message.setSubject(title);
          // 邮件的文本内容
          message.setContent("发送邮件成功！", "text/html;charset=UTF-8");
          // 返回创建好的邮件对象
          return message;
    }
}
